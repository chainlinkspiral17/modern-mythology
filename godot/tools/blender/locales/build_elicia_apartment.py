def _dust_film(tag, cx, cy, z, w, d):
    """One thin ash-grey film slab — 'the dust still settling on
    most surfaces' (line 397)."""
    make_box(f"Dust_{tag}", (cx, cy, z + 0.0013), (w, d, 0.0026), COL_DUST)


def _page_drift(prefix, cx, cy, bz, count, spread_x, spread_y, start=0):
    """Drifts of rejected script pages (line 46)."""
    for i in range(count):
        ox = (((i * 13 + start * 5) % 11) / 11.0 - 0.5) * spread_x
        oy = (((i * 23 + start * 3) % 9) / 9.0 - 0.5) * spread_y
        tint = P.PAPER if (i + start) % 3 else P.PAPER_AGED
        make_box(f"{prefix}_{i}", (cx + ox, cy + oy, bz + 0.002 + i * 0.0022),
                 (0.21, 0.28, 0.003), tint)
        if (i + start) % 4 == 0:   # a strike-through of dead dialogue
            make_box(f"{prefix}_{i}_Line", (cx + ox, cy + oy,
                     bz + 0.0045 + i * 0.0022), (0.13, 0.02, 0.001),
                     P.NEWSPRINT)


def _cable(tag, pts, z, radius=0.010, tint=COL_CABLE):
    """Dormant metallic ivy (line 46): axis-aligned cable run through
    (x, y) waypoints at height z. Consecutive points must differ in
    exactly one of x / y."""
    for i in range(len(pts) - 1):
        (x0, y0), (x1, y1) = pts[i], pts[i + 1]
        if abs(x1 - x0) > 1e-6:
            make_cyl(f"{tag}_{i}", ((x0 + x1) / 2.0, y0, z), radius,
                     abs(x1 - x0) + radius * 2, tint, axis='X', segments=6)
        else:
            make_cyl(f"{tag}_{i}", (x0, (y0 + y1) / 2.0, z), radius,
                     abs(y1 - y0) + radius * 2, tint, axis='Y', segments=6)


def _cable_riser(tag, x, y, z0, z1, radius=0.010, tint=COL_CABLE):
    make_cyl(tag, (x, y, (z0 + z1) / 2.0), radius, abs(z1 - z0) + radius,
             tint, segments=6)


def _data_slate(tag, cx, cy, bz, w, d, cracked=False, dusted=False):
    """One fallen monolith (line 46): dark glass slab + steel edge."""
    make_box(f"{tag}_Glass", (cx, cy, bz + 0.014), (w, d, 0.028),
             COL_SLATE_GLASS)
    make_box(f"{tag}_EdgeS", (cx, cy - d / 2.0 + 0.008, bz + 0.029),
             (w, 0.016, 0.004), COL_SLATE_EDGE)
    make_box(f"{tag}_EdgeE", (cx + w / 2.0 - 0.008, cy, bz + 0.029),
             (0.016, d, 0.004), COL_SLATE_EDGE)
    if cracked:
        make_box(f"{tag}_CrackA", (cx - w * 0.12, cy + d * 0.10, bz + 0.030),
                 (w * 0.55, 0.004, 0.001), COL_SLATE_EDGE)
        make_box(f"{tag}_CrackB", (cx + w * 0.10, cy - d * 0.14, bz + 0.030),
                 (0.004, d * 0.5, 0.001), COL_SLATE_EDGE)
    if dusted:
        _dust_film(f"{tag}", cx - w * 0.15, cy + d * 0.12, bz + 0.028,
                   w * 0.5, d * 0.45)


def _crumple(tag, cx, cy, i):
    """A rejected page, balled (line 46)."""
    s = 0.038 + (i % 3) * 0.007
    tint = P.PAPER if i % 2 else P.PAPER_AGED
    make_box(f"{tag}", (cx, cy, s / 2.0), (s, s * 0.9, s), tint)


def _side_chair(prefix, cx, cy, facing):
    """Slim modern side chair; `facing` = direction the sitter faces
    ('E','S','N','W')."""
    dx, dy = {'N': (0, 1), 'S': (0, -1), 'E': (1, 0), 'W': (-1, 0)}[facing]
    make_box(f"{prefix}_Seat", (cx, cy, 0.44), (0.40, 0.40, 0.035),
             COL_CHARCOAL)
    for li, (sx, sy) in enumerate([(-1, -1), (+1, -1), (-1, +1), (+1, +1)]):
        make_cyl(f"{prefix}_Leg_{li}", (cx + sx * 0.16, cy + sy * 0.16, 0.22),
                 0.014, 0.44, COL_TECH_DARK, segments=6)
    bx, by = cx - dx * 0.185, cy - dy * 0.185
    back_sz = (0.40, 0.03, 0.42) if dx == 0 else (0.03, 0.40, 0.42)
    make_box(f"{prefix}_Back", (bx, by, 0.72), back_sz, COL_CHARCOAL)


