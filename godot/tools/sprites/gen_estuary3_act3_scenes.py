"""Regenerate the nine Estuary 3 Act 3 walkabout hero images.

Deterministic — byte-identical output on every run. Each scene keeps
the compositional subject of the original hand-authored version
(what the location IS) and rebuilds the rendering through the
2026-07-10 HeroImage ops: dithered gradients, shade, noise, disk,
ring, checker. Palette-per-scene (12-14 colors) so each location
holds its own morning: it is Labor Day, September 1, 1998, and the
marine layer is lifting at different rates all over town.

Also emits library/sprites/shelf_backdrop.json — the cabin wall
behind the slowstock shelf in the 2048 frame story.
"""
