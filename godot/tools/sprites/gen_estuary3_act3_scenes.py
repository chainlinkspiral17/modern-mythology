class Scene:
    """Palette-by-name so nine scenes stay readable."""

    def __init__(self, sid, notes):
        self.id = sid
        self.notes = notes
        self.palette = []
        self._idx = {}
        self.layers = []

    def c(self, name, hexv=None):
        if name not in self._idx:
            if hexv is None:
                raise KeyError("color %r not yet defined in %s" % (name, self.id))
            self._idx[name] = len(self.palette)
            self.palette.append(hexv)
        return self._idx[name]

    def add(self, op, **kw):
        d = {"op": op}
        d.update(kw)
        self.layers.append(d)

    def doc(self):
        return {"id": self.id, "notes": self.notes, "w": W, "h": H,
                "palette": self.palette, "layers": self.layers}


# ── shared vocabulary ────────────────────────────────────────────

def spruce(s, x, base_y, height, color):
    """Coastal conifer silhouette: two stacked triangles + trunk."""
    half = max(3, height // 3)
    s.add("poly", points=[[x, base_y - height], [x - half, base_y - height // 3],
                          [x + half, base_y - height // 3]], color=color)
    s.add("poly", points=[[x, base_y - (height * 3) // 4],
                          [x - half - 2, base_y], [x + half + 2, base_y]],
          color=color)
    s.add("vline", x=x, y_range=[base_y, base_y + 2], color=color)


def figure(s, x, base_y, color, h=12):
    """Small standing person, feet at base_y."""
    s.add("rect", xywh=[x - 1, base_y - h + 3, 3, h - 3], color=color)
    s.add("dot", xy=[x, base_y - h + 1], size=2, color=color)


def bike(s, x, base_y, frame_color, wheel_color):
    """Side-view bicycle, wheels on base_y."""
    s.add("ring", xy=[x - 4, base_y - 3], r=3, color=wheel_color)
    s.add("ring", xy=[x + 4, base_y - 3], r=3, color=wheel_color)
    s.add("polyline", points=[[x - 4, base_y - 3], [x, base_y - 7],
                              [x + 4, base_y - 3], [x, base_y - 3],
                              [x - 4, base_y - 3]], color=frame_color)
    s.add("vline", x=x + 4, y_range=[base_y - 9, base_y - 3], color=frame_color)


